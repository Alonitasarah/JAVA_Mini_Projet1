
//Cette class represente la note 
public class Note {
	   
	    //Attribut note 
	    private double valeur;
  
	    //Contructeur pour initialiser la note 
	    public Note(double valeur) {
	        this.valeur = valeur;
	    }

	    //get pour acceder l'attribut et retourne la note
	    public double getValeur() {
	        return valeur;
	    }

	
	

}
